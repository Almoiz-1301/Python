a = 34
b = 2
# Arithmetic Operators
print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)  # float division
print("a // b =", a // b)  # integer division
print("a % b =", a % b)
print("a ** b =", a ** b)  # exponentiation


# Comparison  conditional Operators
print("\nComparison Operators:")
print ("a == b:", a == b)
print ("a != b:", a != b)
print ("a > b:", a > b)
print ("a < b:", a < b)
print ("a >= b:", a >= b)
print ("a <= b:", a <= b)

# Logical Operators
print("\nLogical Operators:")
c = True
d = False
print("c and d:", c and d)
print("d and c:", d and c)
print("c and c:", c and c)
print("c and c:", d and d)

# Logical or operator
print("\nLogical OR Operators:")
print("c or d:", c or d)
print("d or c:", d or c)
print("c or c:", c or c)
print("d or d:", d or d)
# Logical not operator
print("not c:", not c)
print("not d:", not d)

