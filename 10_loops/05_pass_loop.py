# The Pass statement is used as a placeholder for future code. When the pass statement is executed, nothing happens, but you avoid getting an error when empty code is not allowed.
for i in range(1, 11):
    if i == 5:
        pass  # This is a placeholder for future code
    print(i)
# In this example, when i equals 5, the pass statement is executed, and the loop continues without any action taken at that point.